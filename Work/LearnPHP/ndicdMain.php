<script>
  // Get the slider and slides
  const slider = document.querySelector('.slider');
  const slides = document.querySelectorAll('.slide');
  const prevButton = document.querySelector('.prev');
  const nextButton = document.querySelector('.next');

  // Set the interval for automatic sliding
  const interval = 3000; // 3 seconds

  // Set the current slide index
  let currentSlide = 0;

  // Function to slide to the next slide
  function nextSlide() {
      // Remove the active class from the current slide
      slides[currentSlide].classList.remove('active');

      // Increment the current slide index
      currentSlide = (currentSlide + 1) % slides.length;

      // Add the active class to the next slide
      slides[currentSlide].classList.add('active');
  }

  // Function to slide to the previous slide
  function prevSlide() {
      // Remove the active class from the current slide
      slides[currentSlide].classList.remove('active');

      // Decrement the current slide index
      currentSlide = (currentSlide - 1 + slides.length) % slides.length;

      // Add the active class to the previous slide
      slides[currentSlide].classList.add('active');
  }

  // Set the interval for automatic sliding
  setInterval(nextSlide, interval);
</script>

